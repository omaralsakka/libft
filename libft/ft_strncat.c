#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	st;

	i = 0;
	st = ft_strlen(s1);
	while (i < n && s2[i] != '\0')
	{
		s1[st] = s2[i];
		i++;
		st++;
	}
	s1[st] = '\0';
	return (s1);
}
