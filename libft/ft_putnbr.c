#include "libft.h"

void	ft_putnbr(int n)
{
	long int	j;

	j = n;
	if (j < 0)
	{
		j = -j;
		ft_putchar('-');
	}
	if (j > 9)
		ft_putnbr(j / 10);
	ft_putchar((j % 10) + '0');
}
