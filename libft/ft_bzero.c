#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*c;

	c = (unsigned char *) s;
	if (n > 0)
	{
		while (n > 0)
		{
			*(c++) = '\0';
			n--;
		}
	}
}
