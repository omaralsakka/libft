#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	const char	*s;
	char		*d;

	s = src;
	d = dest;
	while (n)
	{
		*d++ = *s++;
		n--;
	}
	return (dest);
}
